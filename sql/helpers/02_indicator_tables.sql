-- Status Indicator Tables (8) ----
DROP TABLE IF EXISTS ACCOUNT_STATUS_IND, GYM_STATUS_IND, EQUIPMENT_STATUS_IND,
  SESSION_STATUS_IND, AVAILABILITY_STATUS_IND, PLAN_STATUS_IND,
  ACCESS_CARD_STATUS_IND, BOOKING_STATUS_IND;

---- 2.1 base template ----
CREATE TABLE ACCOUNT_STATUS_IND (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  code VARCHAR(64) NOT NULL UNIQUE,
  label VARCHAR(255) NOT NULL,
  created_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
  updated_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6)
) ENGINE=InnoDB;

---- 2.2 IND table with same structure ----
CREATE TABLE GYM_STATUS_IND LIKE ACCOUNT_STATUS_IND;
CREATE TABLE EQUIPMENT_STATUS_IND LIKE ACCOUNT_STATUS_IND;
CREATE TABLE SESSION_STATUS_IND LIKE ACCOUNT_STATUS_IND;
CREATE TABLE AVAILABILITY_STATUS_IND LIKE ACCOUNT_STATUS_IND;
CREATE TABLE PLAN_STATUS_IND LIKE ACCOUNT_STATUS_IND;
CREATE TABLE ACCESS_CARD_STATUS_IND LIKE ACCOUNT_STATUS_IND;
CREATE TABLE BOOKING_STATUS_IND LIKE ACCOUNT_STATUS_IND;

